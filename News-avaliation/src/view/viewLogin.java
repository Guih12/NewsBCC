     private Boolean validateLoginAndPassword(String login, String password){
        
        if(login.trim().equals("")|| password.trim().equals("")){
            return true;
        }
        
        return false;
    }
    
    private Boolean validateUser(User user){
        if(user != null) return true;
        
        else 
            return false;
    }
   
    private void handleLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_handleLoginMouseClicked

        User user = new User();
        String log  = login.getText();
        String pass = password.getText();

        if(validateLoginAndPassword(log, pass)){
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatorios", "Alert", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                user = this.userSignController.autenticateUser(log, pass);

                if(validateUser(user)){
                    viewDashboard viewDashBoard = new viewDashboard(user);
                    setVisible(false);
                    viewDashBoard.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario e senha inv√°lidos", "Error", JOptionPane.ERROR_MESSAGE);
                }

            }catch(NoResultException ex){
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_handleLoginMouseClicked

