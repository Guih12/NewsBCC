    private void listNews(){
         List<News> news =  this.newsController.getAll();
          DefaultTableModel noticies =  (DefaultTableModel) TableNews.getModel();
         
         for(News n : news){
            noticies.addRow(new Object[] {n.getTitle(),  n.getContent(), n.getStartDate(), n.getFinalDate()});
         }
    }
    
    
    
